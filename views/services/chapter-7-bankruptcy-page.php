	<p>Chapter 7 is a federal bankruptcy law enacted by the U.S. Congress to provide single and married individuals (including some owners of sole proprietorships) who experience financial difficulty a way to start over.  Filing for Chapter 7 protection allows individual debtors to discharge (not pay) most unsecured debts, debts for  which there is no collateral. Unsecured debts include many but not all purchases made on credit cards. Filing under Chapter 7 normally allows you to keep your assets and then start over.</p>
	<br/>
	<h3>What Does Filing Chapter 7 Discharge?</h3>
	<p>Chapter 7 generally does not discharge student loans, certain IRS debts, support obligations, or debts arising from fraud, the infliction of a willful malicious injury or restitution.  If you decide to file Chapter 7, our lawyers will work with you to create schedules outlining your creditors, assets, income and expenses. These schedules are signed by you and filed with the court along with a voluntary petition for bankruptcy. Once these documents are filed, creditors are automatically legally prohibited from suing, calling or engaging in other collection actions against you.</p>

	<p>It generally takes approximately three and one-half to five months before the Bankruptcy Judge signs the discharge order and you are relieved of all dischargeable debt.  By filing for Chapter 7, you are usually allowed  to keep your home and vehicles provided you continue to make your normal monthly payments to the mortgage or finance company.</p>

